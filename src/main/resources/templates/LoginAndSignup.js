function login() {

    var username = document.getElementById('username').value;
    var password = document.getElementById('password').value;


    console.log('Login clicked. Username:', username, 'Password:', password);
}

function signup() {

    var signupUsername = document.getElementById('signup-username').value;
    var signupPassword = document.getElementById('signup-password').value;

    console.log('Sign Up clicked. Username:', signupUsername, 'Password:', signupPassword);
}
